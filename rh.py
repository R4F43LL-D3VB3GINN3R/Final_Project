from tkinter import *   # importa a biblioteca tkinter
from tkinter import ttk # importa mais funcionalidades do tkinter

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
class RHSCreen(): # inicializa a classe menu
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
    
    def __init__(self):

        self.rhroot = Tk()
        self.menu_screen()

    def menu_screen(self):
        self.rhroot.title("Menu")
        self.rhroot.geometry("400x300")
        Label(self.rhroot, text="Bem-vindo ao Menu!").pack(padx=20, pady=50)
        self.rhroot.mainloop()

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
    
    def run(self):

        self.rhroot.mainloop()

