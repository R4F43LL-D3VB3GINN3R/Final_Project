from tkinter import *   # importa a biblioteca tkinter
from tkinter import ttk # importa mais funcionalidades do tkinter

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
class MenuScreen(): # inicializa a classe menu
#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
    
    def __init__(self):

        self.menuroot = Tk()
        self.menu_screen()

    def menu_screen(self):
        self.menuroot.title("Menu")
        self.menuroot.geometry("400x300")
        Label(self.menuroot, text="Bem-vindo ao Menu!").pack(padx=20, pady=50)
        self.menuroot.mainloop()

#-------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------#
    
    def run(self):

        self.menuroot.mainloop()

